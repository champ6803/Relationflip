                    <p>8. ข้อกำหนดอื่น ๆ</p>
                    <p>8.1. ไม่มีส่วนใดในข้อตกลงและเงื่อนไขการใช้บริการฉบับนี้ที่มีความหมายหรือสื่อความหมายเป็น
                        การจัดตั้งหุ้นส่วน, ผู้ร่วมลงทุน, ตัวแทน, ตัวแทนด้านกฎหมาย, นายจ้างหรือพนักงานใด ๆ
