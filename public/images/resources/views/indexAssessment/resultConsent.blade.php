                        หากเราพบการกระทำดังกล่าว เรามีสิทธิที่จะระงับการใช้บริการของท่าน
                        และระงับการใช้งานเว็บไซต์Relationflip.comของท่านทันที</p>
                    <p>5.11. ท่านผู้รับบริการอนุญาตให้RF Analytical Counselor
                        ที่ให้บริการแก่ท่านสามารถส่งผลประเมินทางการปรึกษาให้กับเราในระหว่างที่รับบริการจากRelationflipท่านทราบว่าการประเมินผลทางการปรึกษานั้นจัดทำขึ้นเพื่อให้ข้อมูลด้านสุขภาพจิตใจและสุขภาพทั่วไปของท่าน
                        และผลการประเมินนี้จะเป็นข้อมูลให้ท่านได้สามารถพูดคุยกับRF Analytical Counselor
                        ในโอกาสต่อไปได้ และผลการประเมินนี้จะถูกเก็บเป็นความลับและเก็บอย่างปลอดภัยที่สุด แต่ทั้งนี้
